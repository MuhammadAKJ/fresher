# NETWORK MANAGEMENT TOOLS

# Wireshark
# A network protocol analyzer that allows administrators to capture and interactively browse the traffic running on a computer network.
sudo apt install wireshark -y

# nmap
# A network scanning tool that provides discovery and security auditing capabilities to identify devices and services on a network.
sudo apt-get install nmap -y

# Netcat
# A versatile networking utility which reads and writes data across network connections, aiding in debugging and network service scripting.
sudo apt-get install netcat